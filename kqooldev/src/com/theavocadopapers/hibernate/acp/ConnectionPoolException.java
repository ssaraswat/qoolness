package com.theavocadopapers.hibernate.acp;

@SuppressWarnings("serial")
public class ConnectionPoolException extends Exception {


	public ConnectionPoolException(final String message) {
		super(message);
	}

	public ConnectionPoolException(final Throwable throwable) {
		super(throwable);
	}

	public ConnectionPoolException(final String message, final Throwable throwable) {
		super(message, throwable);
	}

}
